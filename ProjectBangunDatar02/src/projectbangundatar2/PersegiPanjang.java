public class PersegiPanjang {
    // method
    double luasPersegiPanjang(double panjang, double lebar){
        double luas = panjang * lebar;
        return luas;
    }
    
    double kelilingPersegiPanjang(double panjang, double lebar){
        double keliling = 2 * (panjang + lebar);
        return keliling;
    }
}
